hari = int(input("masukkan angka (1-7): "))
if hari == 1:
    print("hari senin")
elif hari == 2:
    print("hari selasa")
elif hari == 3:
    print("hari rabu")
elif hari == 4:
    print("hari kamis")
elif hari == 5:
    print("hari jumat")
elif hari == 6:
    print("hari sabtu")
elif hari == 7:
    print("hari minggu")
else:
    print("input tidak valid, hanya boleh 1-7")
