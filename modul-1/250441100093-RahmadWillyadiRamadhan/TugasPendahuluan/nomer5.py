# Program sederhana operator aritmatika

a = 15
b = 4

print("Nilai a =", a)
print("Nilai b =", b)

# Penjumlahan
print("a + b =", a + b)

# Pengurangan
print("a - b =", a - b)

# Perkalian
print("a * b =", a * b)

# Pembagian
print("a / b =", a / b)

# Sisa Bagi
print("a % b =", a % b)

# Pangkat
print("a ** b =", a ** b)

# Pembagian Bulat
print("a // b =", a // b)
