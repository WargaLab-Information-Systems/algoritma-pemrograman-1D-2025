# a. Membuat variabel untuk menyimpan harga buku dan harga pulpen
harga_buku = 45000
harga_pulpen = 7500

# b. Menghitung total biaya
jumlah_buku = 3
jumlah_pulpen = 2
total_biaya = (harga_buku * jumlah_buku) + (harga_pulpen * jumlah_pulpen)

# c. Menghitung uang kembalian
pembayaran = 200000
kembalian = pembayaran - total_biaya

# Output hasil
print("Total biaya yang harus dibayar:", total_biaya)
print("Uang kembalian:", kembalian)
